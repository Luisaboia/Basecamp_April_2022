#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	ft_putstr(char *str)
{
	int		i;

	i = 0;
	while (str[i] != '\0')
	{
		ft_putchar(str[i]);
		i++;
	}
}

int	main(int a, char *b[])
{
	int	i;

	i = 0;
	while (++i < a)
	{
		ft_putstr(b[a - i]);
		ft_putstr("\n");
	}
}
